#!/usr/bin/env python3
"""
Plot P2P multi-threaded benchmark results.

This script reads CSV files generated by run_p2p_experiments.sh and creates
bar plots showing the runtime performance for different thread counts and buffer sizes.
"""

import pandas as pd
import matplotlib.pyplot as plt
import numpy as np
import sys
import os

def plot_results(sendrecv_csv, isend_csv, output_dir="./p2p_results"):
    """
    Create bar plots for sendrecv and isend benchmark results.

    Args:
        sendrecv_csv: Path to sendrecv results CSV
        isend_csv: Path to isend results CSV
        output_dir: Directory to save plots
    """
    # Read CSV files
    try:
        df_sendrecv = pd.read_csv(sendrecv_csv)
        df_isend = pd.read_csv(isend_csv)
    except FileNotFoundError as e:
        print(f"Error: Could not find CSV file: {e}")
        return
    except pd.errors.EmptyDataError:
        print("Error: CSV file is empty")
        return
    
    print(df_sendrecv)
    print(df_isend)

    # Create output directory if it doesn't exist
    os.makedirs(output_dir, exist_ok=True)

    # Define plot styling
    colors = ['cadetblue', 'darkviolet', 'navy', 'cyan']
    buffer_sizes = ['1B', '1KB', '1MB', '200MB']

    # Create figure with two subplots
    fig, (ax1, ax2) = plt.subplots(1, 2, figsize=(16, 6))

    # Function to create a plot
    def create_plot(ax, df, title, method_name):
        # Get unique thread counts
        thread_counts = sorted(df['threads'].unique())
        x = np.arange(len(thread_counts))

        # Plot for each buffer size
        for i, buffer_label in enumerate(buffer_sizes):

            # Filter data for this buffer size
            buffer_data = df[df['buffer_label'] == buffer_label]

            # Sort by thread count
            buffer_data = buffer_data.sort_values('threads')

            # Get runtime values
            runtimes = buffer_data['total_time'].values

            ax.plot(x, runtimes, label=buffer_label, color=colors[i], marker='o')

        # Customize plot
        ax.set_xlabel('Number of Threads', fontsize=12)
        ax.set_ylabel('Runtime (seconds)', fontsize=12)
        ax.set_title(title, fontsize=14)
        ax.set_yscale("log")
        ax.set_xticks(x)
        ax.set_xticklabels(thread_counts)
        ax.legend(title='Buffer Size', fontsize=10)
        ax.grid(alpha=0.3)

        # Set y-axis to start from 0
        # ax.set_ylim(bottom=0)


    # SendRecv plot
    fig_sendrecv, ax_sendrecv = plt.subplots(figsize=(10, 6))
    create_plot(ax_sendrecv, df_sendrecv,
                   'MPI_Sendrecv Performance vs Thread Count', 'sendrecv')
    plt.tight_layout()
    output_file_sendrecv = os.path.join(output_dir, 'p2p_sendrecv.png')
    plt.savefig(output_file_sendrecv, dpi=300, bbox_inches='tight')
    print(f"SendRecv plot saved to: {output_file_sendrecv}")

    # Isend plot
    fig_isend, ax_isend = plt.subplots(figsize=(10, 6))
    create_plot(ax_isend, df_isend,
                   'MPI_Isend/Irecv Performance vs Thread Count', 'isend')
    plt.tight_layout()
    output_file_isend = os.path.join(output_dir, 'p2p_isend.png')
    plt.savefig(output_file_isend, dpi=300, bbox_inches='tight')
    print(f"Isend plot saved to: {output_file_isend}")

    # Print summary statistics
    print("\n=== Summary Statistics ===\n")
    print("SendRecv Results:")
    print(df_sendrecv.groupby(['buffer_label', 'threads'])['total_time'].describe())
    print("\nIsend Results:")
    print(df_isend.groupby(['buffer_label', 'threads'])['total_time'].describe())

    # Show plots
    plt.show()

def main():
    if len(sys.argv) < 3:
        print("Usage: python plot_p2p_results.py <sendrecv_csv> <isend_csv> [output_dir]")
        print("\nExample:")
        print("  python plot_p2p_results.py ./p2p_results/sendrecv_results.csv ./p2p_results/isend_results.csv")
        sys.exit(1)

    sendrecv_csv = sys.argv[1]
    isend_csv = sys.argv[2]
    output_dir = sys.argv[3] if len(sys.argv) > 3 else "./p2p_results"

    # Check if files exist
    if not os.path.exists(sendrecv_csv):
        print(f"Error: File not found: {sendrecv_csv}")
        sys.exit(1)
    if not os.path.exists(isend_csv):
        print(f"Error: File not found: {isend_csv}")
        sys.exit(1)

    plot_results(sendrecv_csv, isend_csv, output_dir)

if __name__ == "__main__":
    main()
